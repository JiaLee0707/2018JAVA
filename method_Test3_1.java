import java.util.*;
class method_Test3_1 {
	public void a(int x) {
		if(x>0) {
			System.out.println(x+"은 양수");
		}
		else {
			System.out.println(x+"은 음수");
		}
	}

	public static void main(String ar[]) {
		Scanner sc=new Scanner(System.in);
		method_Test3_1 m1=new method_Test3_1();
		System.out.print("숫자를 입력하시오 : ");
		int y=sc.nextInt();
		m1.a(y);
	}
}